void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
  // DRV驱动器故障
  if (DRV_FAULT_Pin == GPIO_Pin) {
    uint16_t r0, r1;
    DRV8323_GetFaultStatus(&driver, &r0);
    DRV8323_GetVGSStatus(&driver, &r1);
    CAW_LOG_ERROR("DRV FAULT FAULT-REG: %X VGS-REG: %X", r0, r1);
    if (!drv_init_state || !drv_health_state) return;
    drv_health_state = 0;
  }
}

void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan) {
  CAN_RxHeaderTypeDef rxHeader;
  uint8_t data[8];
  HAL_CAN_GetRxMessage(hcan, CAN_FILTER_FIFO0, &rxHeader, data);

  if (CAN1 == hcan->Instance) {
    // CAW_LOG_DEBUG("DLC: %d IDE: %d RTR: %d", rxHeader.DLC, rxHeader.IDE,
    //               rxHeader.RTR);
    // CAW_LOG_DEBUG("data: %x %x %x %x %x %x %x %x", data[0], data[1], data[2],
    //               data[3], data[4], data[5], data[6], data[7]);
    CAN_TxHeaderTypeDef txHeader;
    txHeader.DLC = rxHeader.DLC;
    txHeader.StdId = 0x0000;
    txHeader.RTR = 0x0000;
    txHeader.IDE = 0x0000;
    HAL_CAN_AddTxMessage(hcan, &txHeader, data, CAN_TX_MAILBOX0);
  }
}
